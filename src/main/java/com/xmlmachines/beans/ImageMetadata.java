public class ImageMetadata {

    private String aperture;
    private String credit;
    private String camera;
    private String caption;
    private String created_timestamp;
    private String copyright;
    private String focal_length;
    private String iso;
    private String shutter_speed;
    private String title;
    private String orientation;

    public String getAperture() {
        return aperture;
    }

    public void setAperture(String aperture) {
        this.aperture = aperture;
    }

    public String getCredit() {
        return credit;
    }

    public void setCredit(String credit) {
        this.credit = credit;
    }

    public String getCamera() {
        return camera;
    }

    public void setCamera(String camera) {
        this.camera = camera;
    }

    public String getCaption() {
        return caption;
    }

    public void setCaption(String caption) {
        this.caption = caption;
    }

    public String getCreatedTimestamp() {
        return created_timestamp;
    }

    public void setCreatedTimestamp(String created_timestamp) {
        this.created_timestamp = created_timestamp;
    }

    public String getCopyright() {
        return copyright;
    }

    public void setCopyright(String copyright) {
        this.copyright = copyright;
    }

    public String getFocalLength() {
        return focal_length;
    }

    public void setFocalLength(String focal_length) {
        this.focal_length = focal_length;
    }

    public String getIso() {
        return iso;
    }

    public void setIso(String iso) {
        this.iso = iso;
    }

    public String getShutterSpeed() {
        return shutter_speed;
    }

    public void setShutterSpeed(String shutter_speed) {
        this.shutter_speed = shutter_speed;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getOrientation() {
        return orientation;
    }

    public void setOrientation(String orientation) {
        this.orientation = orientation;
    }


}
